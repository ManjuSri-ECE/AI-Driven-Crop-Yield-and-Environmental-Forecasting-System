    ax_c.set_xticks(x)
    ax_c.set_xticklabels(models_list, fontsize=9)
    ax_c.set_ylabel('Normalised Error (lower = better)', fontsize=9)
    ax_c.set_title('Error Metrics Comparison\n(MSE · RMSE · MAE)', fontsize=11, fontweight='bold')
